# В последовательности на n целых элементов найти количество пар, для которых
# произведение элементов делится на 3 (элементы пары в последовательности являются
# соседними).

def count_pairs_divisible_by_3(arr):
    # Используем списковое включение для создания списка пар
    pairs = [(arr[i], arr[i + 1]) for i in range(len(arr) - 1)]
    # Считаем количество пар, произведение которых делится на 3
    count = sum(1 for x, y in pairs if (x * y) % 3 == 0)
    return count

n = [1, 2, 3, 4, 5, 6]
result = count_pairs_divisible_by_3(n)
print("Количество пар, произведение которых делится на 3:", result)
